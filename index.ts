type Person = {
  name: string;
};

const person: Person = {
  name: "Emmanuel",
};

console.log(person.name);



/**
 * Annotations
 * Syntax: let myVar: type = value
 */

// String
let myName: string = "Emmanuel Enoh";
myName = "Besong Enoh";
console.log(myName);

// Number
let age: number = 22;
age += 2;
console.log(age);

// Boolean
let tsEasy: boolean = false;
console.log(tsEasy); 

